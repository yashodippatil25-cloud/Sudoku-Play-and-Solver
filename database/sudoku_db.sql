-- ── Optional: scores / leaderboard table ──────────────────
CREATE TABLE IF NOT EXISTS scores (
  id         INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  player     VARCHAR(64)  NOT NULL DEFAULT 'Anonymous',
  difficulty ENUM('beginner', 'normal', 'pro') NOT NULL,
  puzzle_id  INT UNSIGNED NOT NULL,
  time_sec   SMALLINT UNSIGNED NOT NULL,
  mistakes   TINYINT UNSIGNED NOT NULL,
  completed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (puzzle_id) REFERENCES sudoku_puzzles(id)
) ENGINE=InnoDB;

SELECT CONCAT('✓ Database seeded. ', COUNT(*), ' puzzles loaded.') AS status
FROM sudoku_puzzles;