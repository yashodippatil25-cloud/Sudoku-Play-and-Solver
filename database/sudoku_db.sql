CREATE DATABASE IF NOT EXISTS sudoku_db
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

USE sudoku_db;

-- ── Puzzles table ──────────────────────────────────────────
CREATE TABLE IF NOT EXISTS sudoku_puzzles (
  id            INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  difficulty    ENUM('beginner', 'normal', 'pro') NOT NULL,
  puzzle_number TINYINT UNSIGNED NOT NULL,   -- 1, 2, or 3
  puzzle        CHAR(81) NOT NULL,           -- 0 = empty cell
  solution      CHAR(81) NOT NULL,
  created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

  UNIQUE KEY uq_level_num (difficulty, puzzle_number)
) ENGINE=InnoDB;

